  /* ─── Snapshot storage ───────────────────────────────────────────────── */
  var snapText  = [];   // [{node, original}]
  var snapAttrs = [];   // [{el, attr, original}]

  function snapshot() {
    snapText  = [];
    snapAttrs = [];

    /* Text nodes */
    var walker = document.createTreeWalker(
      document.body,
      NodeFilter.SHOW_TEXT,
      {
        acceptNode: function (node) {
          /* Skip script, style, noscript */
          var tag = node.parentElement && node.parentElement.tagName.toUpperCase();
          if (['SCRIPT','STYLE','NOSCRIPT'].indexOf(tag) !== -1) return NodeFilter.FILTER_REJECT;
          /* Skip empty nodes */
          if (!node.data.trim()) return NodeFilter.FILTER_SKIP;
          return NodeFilter.FILTER_ACCEPT;
        }
      }
    );
    while (walker.nextNode()) {
      snapText.push({ node: walker.currentNode, orig: walker.currentNode.data });
    }

    /* Attributes: placeholder, aria-label, title, data-suffix */
    var attrEls = document.querySelectorAll('[placeholder],[aria-label],[title],[data-suffix]');
    attrEls.forEach(function (el) {
      ['placeholder','aria-label','title','data-suffix'].forEach(function (attr) {
        var val = el.getAttribute(attr);
        if (val && val.trim()) {
          snapAttrs.push({ el: el, attr: attr, orig: val });
        }
      });
    });
  }

  /* ─── Apply translations ─────────────────────────────────────────────── */
  function normalize(s) {
    return s.replace(/\s+/g, ' ').trim();
  }

  function resolveTranslation(dict, key) {
    if (dict[key] !== undefined) return dict[key];

    var core = key;
    var prefix = '';
    var suffix = '';
    /* Allow existing icon/arrow wrappers to reuse the same dictionary entry. */
    var prefixMatch = core.match(/^([^A-Za-zА-Яа-яЁё0-9№]+\s*)(.+)$/);
    if (prefixMatch) {
      prefix = prefixMatch[1];
      core = prefixMatch[2];
    }
    var suffixMatch = core.match(/^(.+?)(\s*[^A-Za-zА-Яа-яЁё0-9№]+)$/);
    if (suffixMatch) {
      core = suffixMatch[1];
      suffix = suffixMatch[2];
    }

    if ((prefix || suffix) && dict[core] !== undefined) {
      return prefix + dict[core] + suffix;
    }

    return undefined;
  }

  function applyLang(lang) {
    var dict = (lang === 'ru') ? null : T[lang];

    /* Restore all snapshots first */
    snapText.forEach(function (entry) {
      entry.node.data = entry.orig;
    });
    snapAttrs.forEach(function (entry) {
      entry.el.setAttribute(entry.attr, entry.orig);
    });

    if (dict) {
      /* Translate text nodes */
      snapText.forEach(function (entry) {
        var key = normalize(entry.orig);
        var translated = resolveTranslation(dict, key);
        if (translated !== undefined) {
          /* Preserve leading/trailing whitespace */
          var leading  = entry.orig.match(/^\s*/)[0];
          var trailing = entry.orig.match(/\s*$/)[0];
          entry.node.data = leading + translated + trailing;
        }
      });

      /* Translate attributes */
      snapAttrs.forEach(function (entry) {
        var key = normalize(entry.orig);
        var translated = resolveTranslation(dict, key);
        if (translated !== undefined) {
          entry.el.setAttribute(entry.attr, translated);
        }
      });

      /* Load CJK font */
      loadCJKFont(lang);
    }

    /* Update <html lang=""> */
    document.documentElement.lang = lang === 'ru' ? 'ru' : lang;

    /* Update page title & meta description */
    var page = (window.location.pathname.split('/').pop() || 'index.html');
    var pageMeta = PAGE_META[page] && PAGE_META[page][lang];
    if (pageMeta) {
      if (pageMeta.title) document.title = pageMeta.title;
      var metaEl = document.querySelector('meta[name="description"]');
      if (metaEl && pageMeta.desc) metaEl.setAttribute('content', pageMeta.desc);
    }

    /* Update CJK body font */
    var fontMap = { zh: "'Noto Sans SC',sans-serif", ja: "'Noto Sans JP',sans-serif", ko: "'Noto Sans KR',sans-serif" };
    document.body.style.fontFamily = fontMap[lang] || '';

    updateSwitcherUI(lang);
  }

  /* ─── Language switcher UI ───────────────────────────────────────────── */
  function updateSwitcherUI(lang) {
    var meta = LANGS[lang] || LANGS.ru;
    var flagEl = document.getElementById('langFlag');
    var codeEl = document.getElementById('langCode');
    var btn    = document.getElementById('langBtn');
    if (flagEl) flagEl.textContent = meta.flag;
    if (codeEl) codeEl.textContent = meta.code;
    if (btn)    btn.setAttribute('aria-label', (T[lang] && T[lang]['Выбрать язык']) || 'Выбрать язык');
    document.querySelectorAll('#langDropdown [data-lang]').forEach(function (b) {
      b.setAttribute('data-active', b.dataset.lang === lang ? 'true' : 'false');
    });
  }

  /* ─── Persist & read language ────────────────────────────────────────── */
  function getLang() {
    var saved = localStorage.getItem('ps-lang');
    if (saved && LANGS[saved]) return saved;
    var browser = (navigator.language || 'ru').slice(0, 2).toLowerCase();
    if (browser === 'zh') return 'zh';
    if (browser === 'ja') return 'ja';
    if (browser === 'ko') return 'ko';
    /* English-speaking browsers: still default to Russian (user can switch manually) */
    return 'ru';
  }

  function setLang(lang) {
    if (!LANGS[lang]) return;
    localStorage.setItem('ps-lang', lang);
    applyLang(lang);
    /* Close dropdown */
    var dd  = document.getElementById('langDropdown');
    var btn = document.getElementById('langBtn');
    if (dd)  dd.classList.remove('open');
    if (btn) btn.setAttribute('aria-expanded', 'false');
  }

  /* ─── Switcher events ────────────────────────────────────────────────── */
  function initSwitcher() {
    var btn = document.getElementById('langBtn');
    var dd  = document.getElementById('langDropdown');
    if (!btn || !dd) return;

    btn.addEventListener('click', function (e) {
      e.stopPropagation();
      var open = dd.classList.toggle('open');
      btn.setAttribute('aria-expanded', open ? 'true' : 'false');
    });

    dd.querySelectorAll('[data-lang]').forEach(function (b) {
      b.addEventListener('click', function () { setLang(b.dataset.lang); });
    });

    document.addEventListener('click', function (e) {
      if (!btn.contains(e.target) && !dd.contains(e.target)) {
        dd.classList.remove('open');
        btn.setAttribute('aria-expanded', 'false');
      }
    });

    document.addEventListener('keydown', function (e) {
      if (e.key === 'Escape' && dd.classList.contains('open')) {
        dd.classList.remove('open');
        btn.setAttribute('aria-expanded', 'false');
        btn.focus();
      }
    });
  }

  /* ─── Public API ─────────────────────────────────────────────────────── */
  window.PSi18n = {
    setLang: setLang,
    getLang: getLang,
    t: function (key) {
      var lang = getLang();
      return (T[lang] && T[lang][key]) || (T.ru && T.ru[key]) || key;
    }
  };

  /* ─── Initialise ─────────────────────────────────────────────────────── */
  document.addEventListener('DOMContentLoaded', function () {
    snapshot();
    initSwitcher();
    var lang = getLang();
    if (lang !== 'ru') applyLang(lang);
    else updateSwitcherUI('ru');
  });

